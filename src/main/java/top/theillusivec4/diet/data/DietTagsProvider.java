  protected void addOptional(TagsProvider.TagAppender<Item> builder, String name) {

    if (name.startsWith("#")) {
      throw new IllegalArgumentException(
          "Attempted to use optional tag: " + name +
              "! Do not use optional tags, this causes weird issues to occur with the Diet tagging system.");
    }
    builder.addOptional(new ResourceLocation(name));
  }

  protected TagKey<Item> tag(String name) {
    return ItemTags.create(new ResourceLocation(DietMod.MOD_ID, name));
  }

  @SafeVarargs
  protected static <T> List<T> createList(Comparator<T> comparator, T... names) {
    List<T> result = Lists.newArrayList(names);
    result.sort(comparator);
    return result;
  }

  protected static List<String> createList(String... names) {
    return createList(Comparator.comparing(String::toString), names);
  }

  protected static List<Item> createList(Item... items) {
    return createList(Comparator.comparing(e -> Objects.requireNonNull(e.builtInRegistryHolder().key().location())),
        items);
  }
